#include "libft.h"

void	ft_putendl_fd(char *c, int n)
{
	int	i;

	i = 0;
	if (!c)
		return ;
	while (c[i] != '\0')
	{
		write(n, &c[i], 1);
		i++;
	}
	write(n, "\n", 1);
}
